export const PRODUCT_TRANSLATIONS: Record<ProductLocale, TranslationTable> = { en: EN, pl: PL };
export const PRODUCT_TRANSLATION_KEYS = Object.keys(EN) as TranslationKey[];

type LocaleContextValue = {
  locale: ProductLocale;
  setLocale: (locale: ProductLocale) => void;
  t: (key: TranslationKey, variables?: Record<string, string | number>) => string;
};

const LocaleContext = createContext<LocaleContextValue>({
  locale: DEFAULT_PRODUCT_LOCALE,
  setLocale: () => undefined,
  t: (key, variables) => interpolate(EN[key], variables),
});

export function ProductLocaleProvider({
  children,
  initialLocale,
}: {
  children: ReactNode;
  initialLocale?: ProductLocale;
}) {
  const [locale, setLocaleState] = useState<ProductLocale>(() => (
    initialLocale ?? readStoredProductLocale()
  ));

  const setLocale = useCallback((nextLocale: ProductLocale) => {
    setLocaleState(nextLocale);
    applyProductLocale(nextLocale);
  }, []);

  useEffect(() => {
    applyProductLocale(locale);
  }, [locale]);

  const value = useMemo<LocaleContextValue>(() => ({
    locale,
    setLocale,
    t: (key, variables) => interpolate(PRODUCT_TRANSLATIONS[locale][key], variables),
  }), [locale, setLocale]);

  return <LocaleContext.Provider value={value}>{children}</LocaleContext.Provider>;
}

export function useProductLocale(): LocaleContextValue {
  return useContext(LocaleContext);
}

export function readStoredProductLocale(storage: Pick<Storage, "getItem"> | null = getBrowserStorage()): ProductLocale {
  const stored = storage?.getItem(PRODUCT_LOCALE_STORAGE_KEY);
  return stored === "pl" || stored === "en" ? stored : DEFAULT_PRODUCT_LOCALE;
}

export function applyProductLocale(
  locale: ProductLocale,
  storage: Pick<Storage, "setItem"> | null = getBrowserStorage(),
  documentElement: Pick<HTMLElement, "lang"> | null = getDocumentElement(),
): void {
  storage?.setItem(PRODUCT_LOCALE_STORAGE_KEY, locale);
  if (documentElement) documentElement.lang = locale;
}

export function formatProductDateTime(value: string, locale: ProductLocale): string {
  const date = new Date(value);
  if (Number.isNaN(date.getTime())) return value;
  return date.toLocaleString(locale === "pl" ? "pl-PL" : "en-US", {
    dateStyle: "short",
    timeStyle: "short",
  });
}

export function formatProductTime(value: string, locale: ProductLocale): string {
  const date = new Date(value);
  if (Number.isNaN(date.getTime())) return value;
  return date.toLocaleTimeString(locale === "pl" ? "pl-PL" : "en-US", {
    hour: "2-digit",
    minute: "2-digit",
  });
}

export function formatProductAge(seconds: number, locale: ProductLocale): string {
  const normalized = Math.max(0, Math.floor(seconds));
  if (normalized < 60) return locale === "pl" ? `${normalized} s` : `${normalized} sec`;
  if (normalized < 3600) return `${Math.floor(normalized / 60)} min`;
  const hours = Math.floor(normalized / 3600);
  return locale === "pl" ? `${hours} godz.` : `${hours} hr`;
}

export function formatProductPairAge(
  pairAgeDays: number | null,
  locale: ProductLocale,
  missing: string,
  options: { pairCreatedAt?: string | null; now?: Date } = {},
): string {
  const now = options.now ?? new Date();
  const createdAtMs = options.pairCreatedAt ? Date.parse(options.pairCreatedAt) : Number.NaN;
  const elapsedHours = Number.isFinite(createdAtMs)
    ? (now.getTime() - createdAtMs) / (60 * 60 * 1_000)
    : pairAgeDays !== null && Number.isFinite(pairAgeDays) ? pairAgeDays * 24 : null;
  return elapsedHours === null ? missing : formatProductAgeHours(elapsedHours, locale);
}

export function formatProductElapsedSince(
  value: string,
  now: Date,
  locale: ProductLocale,
  missing: string,
): string {
  const timestamp = Date.parse(value);
  if (!Number.isFinite(timestamp)) return missing;
  return formatProductAgeHours((now.getTime() - timestamp) / (60 * 60 * 1_000), locale);
}

export function formatProductAgeHours(hours: number, locale: ProductLocale): string {
  const normalized = Math.max(0, hours);
  if (normalized < 1) return locale === "pl" ? "mniej niż 1 godz." : "less than 1 hour";
  const fullHours = Math.floor(normalized);
  if (fullHours < 24) {
    if (locale === "pl") return `${fullHours} godz.`;
    return `${fullHours} ${fullHours === 1 ? "hour" : "hours"}`;
  }
  const days = Math.floor(fullHours / 24);
  if (locale === "pl") return `${days} ${days === 1 ? "dzień" : "dni"}`;
  return `${days} ${days === 1 ? "day" : "days"}`;
}

export function formatFollowUpLifecycleStatus(value: FollowUpLifecycleStatus, locale: ProductLocale): string {
  const labels: Record<FollowUpLifecycleStatus, [string, string]> = {
    NEW: ["New", "Nowe"],
    MATURING: ["Maturing", "Dalsza obserwacja"],
    CANDIDATE_FOR_ESTABLISHED: ["Candidate for Established", "Kandydaci do Established"],
    ESTABLISHED: ["Established", "Established"],
    ARCHIVED: ["Archived", "Archiwalne"],
  };
  return labels[value][locale === "pl" ? 1 : 0];
}

export function formatProductUsd(value: number | null, locale: ProductLocale, missing: string): string {
  if (value == null) return missing;
  return new Intl.NumberFormat(locale === "pl" ? "pl-PL" : "en-US", {
    style: "currency",
    currency: "USD",
    maximumFractionDigits: value < 1 ? 6 : 0,
  }).format(value);
}

function interpolate(template: string, variables?: Record<string, string | number>): string {
  if (!variables) return template;
  return template.replace(/\{(\w+)\}/g, (match, key: string) => (
    Object.prototype.hasOwnProperty.call(variables, key) ? String(variables[key]) : match
  ));
}

function getBrowserStorage(): Storage | null {
  if (typeof window === "undefined") return null;
  try {
    return window.localStorage;
  } catch {
    return null;
  }
}

function getDocumentElement(): HTMLElement | null {
  return typeof document === "undefined" ? null : document.documentElement;
}
